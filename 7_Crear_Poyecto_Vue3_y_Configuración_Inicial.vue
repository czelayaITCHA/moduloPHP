## 1. Crear proyecto Vue 3
**¿Que es Vue 3? ** es la última versión del framework JavaScript progresivo para construir interfaces de usuario, lanzado en 2020. Destaca por ser más rápido, pequeño y eficiente que Vue 2, introduciendo la Composition API para organizar código complejo, un mejor soporte para TypeScript y un sistema de reactividad basado en Proxies ES6. 
